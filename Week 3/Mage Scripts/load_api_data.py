@data_loader
def load_data_from_api(*args, **kwargs):
    """
    Template for loading data from API
    """
    year_month = ["2022-01","2022-02","2022-03","2022-04","2022-05","2022-06","2022-07","2022-08","2022-09","2022-10","2022-11","2022-12"]

    output = pd.DataFrame()
    
    for x in year_month:
        df = pd.read_parquet(path=f"https://d37ci6vzurychx.cloudfront.net/trip-data/green_tripdata_{x}.parquet")
        output = pd.concat([output, df])
    
    return output


